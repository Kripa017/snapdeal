export function buildStaticProductUrl(item) {
  const params = new URLSearchParams();
  params.set('name', item.name);
  params.set('price', String(item.price));
  params.set('mrp', String(item.mrp));
  params.set('discount', item.discount);
  params.set('colour', item.colour);
  params.set('size', item.size);
  params.set('brand', item.brand);
  params.set('pack', item.pack);
  params.set('rating', item.rating);
  params.set('image', item.image);
  params.set('description', item.description || '');
  return `/product?${params.toString()}`;
}

export function buildApiProductUrl(p) {
  if (p._id) {
    return `/product?id=${encodeURIComponent(String(p._id))}`;
  }
  const imageUrl = p.image
    ? `http://localhost:3001/uploads/${p.image}`
    : '';
  const params = new URLSearchParams();
  params.set('name', p.product);
  params.set('price', String(p.price));
  params.set('mrp', String(p.mrp ?? p.price));
  params.set('discount', p.discount || '');
  params.set('colour', p.colour || 'Various');
  params.set('size', p.size || 'Standard');
  params.set('brand', p.brand || 'Snapdeal');
  params.set('pack', 'Single');
  params.set('rating', String(p.rating ?? '4'));
  params.set('ratingCount', String(p.ratingCount ?? '0'));
  params.set('image', imageUrl);
  params.set('description', p.description || '');
  params.set('category', p.category || '');
  params.set('pattern', p.pattern || 'solid');
  return `/product?${params.toString()}`;
}

/** Shape expected by Product page + cart (uploaded / API products) */
export function apiProductToCartItem(p) {
  const imageUrl = p.image
    ? `http://localhost:3001/uploads/${p.image}`
    : '';
  const price = Number(p.price);
  const mrp = p.mrp != null ? Number(p.mrp) : price;
  return {
    name: p.product,
    price,
    mrp,
    discount: p.discount || '',
    rating: String(p.rating ?? '4'),
    ratingCount: String(p.ratingCount ?? '0'),
    image: imageUrl,
    size: p.size || 'Standard',
    colour: p.colour || 'Various',
    brand: p.brand || 'Snapdeal',
    pattern: p.pattern || 'solid',
    description: p.description || 'No description available',
    quantity: 1,
  };
}
